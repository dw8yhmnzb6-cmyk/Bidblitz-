// Helper function
export const getPageTranslation = (lang, page, key) => {
  const langCode = lang === 'ae' ? 'ar' : lang === 'xk' ? 'sq' : lang === 'gb' ? 'en' : lang;
  const translations = pageTranslations[langCode] || pageTranslations['en'];
  return translations?.[page]?.[key] || pageTranslations['en']?.[page]?.[key] || key;
};
